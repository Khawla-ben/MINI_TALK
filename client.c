#include "minitalk.h"

int	main(int ac, char **av)
{
	int		i;
	int		num;
	char	c;

	if ((ac == 3) && (av[1] > 0))
	{
		i = -1;
		while (av[2][++i])
		{
			c = av[2][i];
			num = 8;
			if (c < 0 || c > 127)
				return (0);
			while (--num >= 0)
			{
				kill(ft_atoi(av[1]), ((c >> num) & 1) + 30);
				usleep(800);
			}
		}
	}
}
